	public function fetch($id, $fk=0)
	{
		dol_syslog(__METHOD__, LOG_DEBUG);

		$sql = 'SELECT';
		$sql .= ' t.rowid,';
		
		$sql .= " t.fk_calendar,";
		$sql .= " t.working_day,";
		$sql .= " t.working_day_hours,";
		$sql .= " t.nonwork_day,";
		$sql .= " t.hours_day,";
		$sql .= " t.hours_week,";
